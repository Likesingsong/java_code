package basics;

/*
 * VariableExample2
 * This class continues to demonstrate variable usage with additional examples.
 */
public class VariableExample2 {
    public static void main(String[] args) {
        // Example of using different variable types
        int integerVariable = 10;
        double doubleVariable = 20.5;
        char characterVariable = 'A';
        String stringVariable = "Hello, World!";

        // Displaying the values of the variables
        System.out.println("Integer Variable: " + integerVariable);
        System.out.println("Double Variable: " + doubleVariable);
        System.out.println("Character Variable: " + characterVariable);
        System.out.println("String Variable: " + stringVariable);
    }
}