package basics;

/*
 * VariableExample
 * This class illustrates the usage of different variable types in Java.
 */
public class VariableExample {
    public static void main(String[] args) {
        // Example of integer variable
        int integerVariable = 10;
        System.out.println("Integer Variable: " + integerVariable);

        // Example of double variable
        double doubleVariable = 20.5;
        System.out.println("Double Variable: " + doubleVariable);

        // Example of character variable
        char charVariable = 'A';
        System.out.println("Character Variable: " + charVariable);

        // Example of boolean variable
        boolean booleanVariable = true;
        System.out.println("Boolean Variable: " + booleanVariable);
    }
}