package basics;

public class VariableExample3 {
    public static void main(String[] args) {
        // Example of different variable types and operations
        int integerVariable = 10;
        double doubleVariable = 20.5;
        char characterVariable = 'A';
        boolean booleanVariable = true;

        // Displaying the values of the variables
        System.out.println("Integer Variable: " + integerVariable);
        System.out.println("Double Variable: " + doubleVariable);
        System.out.println("Character Variable: " + characterVariable);
        System.out.println("Boolean Variable: " + booleanVariable);

        // Performing some operations
        double sum = integerVariable + doubleVariable;
        System.out.println("Sum of Integer and Double: " + sum);
    }
}